/*
 * pigura_mulai — Inisialisasi pigura dalam mode teks VGA.
 * Dipanggil sekali oleh arsitek_mulai() setelah konstruksi selesai.
 * Menyiapkan konsol teks 80x25 pada alamat VGA 0xB8000.
 */
int pigura_mulai(void)
{
#if defined(ARSITEK_X86) || defined(ARSITEK_X64)
    /* Siapkan mode teks VGA */
    konsol.mode = PIGURA_MODE_TEKS;
    konsol.lebar = VGA_LEBAR;
    konsol.tinggi = VGA_TINGGI;
    konsol.kursor_x = 0;
    konsol.kursor_y = 0;
    konsol.warna_depan = VGA_PUTIH;
    konsol.warna_latar = VGA_HITAM;
    konsol.alamat = VGA_ALAMAT;
    konsol.kursor_nyala = BENAR;

    /* Isi mode tampilan */
    mode_aktif.lebar = VGA_LEBAR;
    mode_aktif.tinggi = VGA_TINGGI;
    mode_aktif.bit_per_piksel = 4;
    mode_aktif.piksel_per_baris = VGA_LEBAR;
    mode_aktif.alamat = VGA_ALAMAT;
    mode_aktif.ukuran_buffer = (ukuran_t)(VGA_LEBAR * VGA_TINGGI * 2);

    /* Hapus layar */
    pigura_hapus_layar();

#else
    /* Pada ARM, pigura belum didukung tanpa framebuffer yang diketahui */
    konsol.mode = PIGURA_MODE_GRAFIS;
    konsol.lebar = 0;
    konsol.tinggi = 0;
#endif

    pigura_siap = BENAR;
    return STATUS_OK;
}

